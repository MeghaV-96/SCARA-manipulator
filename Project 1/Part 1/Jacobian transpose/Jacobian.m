function y = Jacobian(q)
j11 = -0.5*(sin(q(1) + q(2))+sin(q(1)));
j12 = -0.5*sin(q(1)+q(2));
j13 = 0;
j14 = 0;

j21 = 0.5*(cos(q(1))+q(2))+cos(q(1));
j22 = 0.5*cos(q(1)+q(2));
j23 = 0;
j24 = 0;

j31 = 0;
j32 = 0;
j33 = -1;
j34 = 0;

j41 = 1;
j42 = 1;
j43 = 0;
j44 = 1;
y = [j11,j12,j13,j14;j21,j22,j23,j24;j31,j32,j33,j34;j41,j42,j43,j44];
end